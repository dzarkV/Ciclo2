    /**
     * Método para crear un nuevo DT
     * @param evt 
     */
    private void buttonCreateDTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonCreateDTActionPerformed
        DTController dtc = new DTController();
        Integer id = null;
        try{
            if(!"".equals(nameCreateDT.getText()) || !"".equals(lastnameCreateDT.getText()) ){
            
                id = Integer.parseInt(idCreateDT.getText()) ;
                String nombre = nameCreateDT.getText();
                String apellido = lastnameCreateDT.getText();
                java.util.Calendar jud =  birthdateCreateDT.getCalendar() ;
                Date sqlDate = fromCalendartoSql(jud);
        
                Integer mesContrato = monthCreateDT.getMonth() + 1 ;
                Integer anioContrato = yearCreateDT.getYear();
                
                DT dt = new DT(id, nombre, apellido, sqlDate, mesContrato, anioContrato);
                dtc.saveDT(dt);
        
                //Para establecer el mismo id del DT en Tab Equipo
                idDT = Integer.parseInt(idCreateDT.getText()) ;
                DTnameCreateEquipo.setText(idDT.toString());
        
                JOptionPane.showMessageDialog(null, "El director técnico fue creado con exito \n Ahora puede asignarle un equipo en la siguiente pestaña");
            } else{
                JOptionPane.showMessageDialog(null, "El director técnico no se pudo crear, por favor llene los datos");
            }
        } catch (HeadlessException | NumberFormatException ex){
            JOptionPane.showMessageDialog(null, "El director técnico no se pudo crear, por favor llene los datos");
        }
    }//GEN-LAST:event_buttonCreateDTActionPerformed

