    /**
     * Método para adaptar el tipo de dato de la fecha de la vista al tipo de dato aceptado en BD Sql
     * @param c Entra el tipo de dato recogido por el JCalendar
     * @return Obtiene dato tipo Date Sql con el formato para la BD
     */
    protected static Date fromCalendartoSql(Calendar c){
        /* Se crea instancia de clase Calendar */
        Calendar calendar = Calendar.getInstance();
        calendar.clear();
        /* Asignación de variables a objeto calendario para dar formato*/
        int year = c.get(Calendar.YEAR);
        int month = c.get(Calendar.MONTH );
        int day = c.get(Calendar.DAY_OF_MONTH);
        calendar.set(Calendar.YEAR, year );
        calendar.set(Calendar.MONTH, month);
        calendar.set(Calendar.DAY_OF_MONTH, day);
        java.sql.Date sqlDate = new java.sql.Date(calendar.getTimeInMillis()) ;
        return sqlDate;
    }
    
